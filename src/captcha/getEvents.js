/**
 * @returns {string}
 */
export default function getEvents() {
    const x = events.x;
    const y = events.y;
    const buttons = events.buttons;
    const elapsedMs = events.elapsedMs;

    const u16 = new Int16Array([].concat(rleEncode(x), rleEncode(y), rleEncode(buttons), rleEncode(elapsedMs)));
    const u8 = new Uint8Array(u16.buffer, u16.byteOffset, u16.byteLength);
    const compressed = pako.deflate(u8);
    // Remove zlib magic header
    compressed[0] ^= 150;
    compressed[1] ^= 181;

    return Buffer.from(compressed).toString("base64");
}

/**
 * Run-length encoding maybe?
 * This function compresses an input array by replacing repeated consecutive
 * values with a count-value pair or negative count followed by all repeated elements.
 * The compressed data is returned as an array.
 * @see https://en.wikipedia.org/wiki/Run-length_encoding
 * @param {number[]} input
 * @returns {number[]}
 */
function rleEncode(input) {
    if(input.length === 0) {
        return [0];
    }

    let r = input[0];
    let n = 1;
    let i = [r];
    const output = [];

    for(const s of input.slice(1)) {
        if(s === r) {
            if(i.length > 1 || i.length >= 32767) {
                i.pop();
                output.push(-i.length);
                output.push.apply(output, i);
                i = [];
            }

            n++;
        } else if(n > 1 || n >= 32767) {
            output.push(n);
            output.push(r);
            n = 1;
            i = [s];
        } else {
            i.push(s);
        }

        r = s;
    }

    if(i.length > 1) {
        output.push(-i.length);
        output.push.apply(output, i);
    } else {
        output.push(n);
        output.push(r);
    }

    output.push(0);

    return output;
}
